package org.the.ems.core.config;

import java.lang.annotation.Documented;
import java.lang.annotation.ElementType;
import java.lang.annotation.Inherited;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

@Documented
@Inherited
@Retention(RetentionPolicy.RUNTIME)
@Target({ElementType.FIELD, ElementType.METHOD})
public @interface Configuration {

	public final static String VALUE_DEFAULT = "VALUE_DEFAULT";
	public final static String SECTION_DEFAULT = "SECTION_DEFAULT";

	String[] value() default VALUE_DEFAULT;
	String section() default SECTION_DEFAULT;
	boolean mandatory() default true;
	double scale() default 1;
}
